package cars.lib;

public class CarInfo
{
	public String name;
	public String make;
	public String price;
	public String madeIn;
	
	public CarInfo(String carName, String carMake, String carPrice, String carMadeIn)
	{
		name = carName;
		make = carMake;
		price = carPrice;
		madeIn = carMadeIn;
				
	}
}
